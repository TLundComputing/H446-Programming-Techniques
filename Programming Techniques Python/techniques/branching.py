# Branching in Python is completed by using if statements
# An if-statement are constructed using the if and else keywords
age = 15
if age >= 18:
    print("you are an adult")
else:
    print("You are a child")

# You can check multiple conditions using the keyword elif
if age >= 18:
    print("You are an adult")
elif age >= 13:
    print("You are a teenage/young adult")
else:
    print("You are a child")
