# Python is a loosely typed language meaning you don't need to declare the data type
# To declare a variable you state the identifier and then the value after the equals

name = "TLundComputing"
age = 27

# You can print variables by wrapping them in a print statement
print(name)

# To print variables with string literals then you separate strings and variables with commas
print("My name is", name, "and I am", age, "years old!")
